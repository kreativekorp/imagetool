package com.kreative.imagetool.transform;

public class PebblePalette extends ColorFilter {
	public int transform(int pixel) {
		int a = (pixel >> 24) & 0xFF;
		int r = (pixel >> 16) & 0xFF;
		int g = (pixel >>  8) & 0xFF;
		int b = (pixel >>  0) & 0xFF;
		a = (a < 0x40) ? 0x00 : (a < 0x90) ? 0x55 : (a < 0xE0) ? 0xAA : 0xFF;
		r = (r < 0x40) ? 0x00 : (r < 0x90) ? 0x55 : (r < 0xE0) ? 0xAA : 0xFF;
		g = (g < 0x40) ? 0x00 : (g < 0x90) ? 0x55 : (g < 0xE0) ? 0xAA : 0xFF;
		b = (b < 0x40) ? 0x00 : (b < 0x90) ? 0x55 : (b < 0xE0) ? 0xAA : 0xFF;
		return (a << 24) | (r << 16) | (g << 8) | b;
	}
}
